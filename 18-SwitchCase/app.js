//Break komutu ile durdurmak gerek. Switch case tip değeri de yapar.Tipleride eşleşmelidir.


const not = "AA";

switch(not)
{
    case 'AA':
        console.log("En yüksek not");
        break;
    case 'BA':
        console.log("Süper not");
        break;
    case 'BB':
        console.log("Güzel not");
        break;
    case 'CB':
        console.log("İdare eder not");
        break;
    case 'CC':
        console.log("Hadi yine iyisin");
        break;
    default:
        console.log("Dersten kaldın");
        break;
}



